export default class User {
	constructor (
		id, 
		name, 
		age, 
		sex, 
		city,
		height, 
		weight,
		online,
		lastChanged
		) 
		{
		this.id = id
		this.name = name
		this.age = age
		this.sex = sex
		this.city = city
		this.height = height
		this.weight = weight
		this.online = online
		this.lastChanged = lastChanged
 	}
}